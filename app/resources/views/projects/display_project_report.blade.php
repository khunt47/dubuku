<br>
1. Burndown Chart
<br>
Purpose: Track sprint progress.
<br>
Graph shows: Remaining work (in story points or issue count) vs. time.
<br>
Use: Check if the team is on track to complete all sprint tasks.
<br><br>
2. Burnup Chart
<br>
Purpose: Show work completed over time.
<br>
Graph shows: Total work vs. completed work.
<br>
Use: Visualizes progress and scope changes (if more issues are added mid-sprint).
<br><br>
3. Velocity Chart
<br>
Purpose: Measure team productivity across sprints.
<br>
Graph shows: Amount of work completed in past sprints.
<br>
Use: Helps predict how much work the team can handle in future sprints.
<br><br>
4. Sprint Report
<br>
Purpose: Detail sprint outcomes.
<br>
Content:
<br>
Issues completed and not completed.
<br>
Changes made during the sprint.
<br>
Use: Evaluate what got done vs. what was planned.
<br><br>
5. Control Chart
<br>
Purpose: Measure cycle time for issues.
<br>
Graph shows: Time taken to complete tasks from start to finish.
<br>
Use: Identify bottlenecks in your workflow.
<br><br>
6. Cumulative Flow Diagram
<br>
Purpose: Track progress of issues across workflow stages.
<br>
Graph shows: Number of issues in each status over time.
<br>
Use: Spot workflow bottlenecks and understand task distribution.
<br><br>
7. Created vs. Resolved Issues Report
<br>
Purpose: Compare issue inflow vs. outflow.
<br>
Graph shows: Number of issues created vs. resolved over time.
<br>
Use: See if the team is keeping up with incoming work.
<br><br>
8. Pie Chart Report
<br>
Purpose: Categorize issues for quick insights.
<br>
Graph shows: Breakdown by status, assignee, priority, type, etc.
<br>
Use: Quick visual snapshot of project distribution.
<br><br>
9. Issue Statistics Report
<br>
Purpose: Summarize issues by a chosen field.
<br>
Graph shows: Bar chart of issues grouped by assignee, priority, type, or other fields.
<br>
Use: Understand workload or focus areas.
<br><br>
10. Time Tracking Report
<br>
Purpose: Track work logged on issues.
<br>
Graph shows: Estimated vs. actual time spent on issues.
<br>
Use: Check accuracy of time estimates and team performance.
<br><br>
11. User Workload / Version Workload Reports
<br>
Purpose: Monitor individual or version-level workload.
<br>
Graph shows: Issues assigned per user or per version.
<br>
Use: Ensure no one is overloaded, and planned releases are feasible.
<br><br>